// implement a recursive depth-first-search algorithm
// that takes a node and returns an array
// containing the values of all the visited nodes
//
// WHY? - to solidify the concept of capturing the return value of a recursive function call

module.exports = function treeToArray(node) {
  // this function takes a node and returns and array
  // so initialize the return value
  var result = [];
  result.push(node.value);

  node.children.forEach(child => {
    result = result.concat(treeToArray(child));
  });

  return result;
};
